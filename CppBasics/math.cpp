#include<iostream>
#include<cmath> 

using namespace std;  // math library

int main(){
    // max value
    cout<< max(3,6)<<endl;

    // miv value
    cout<< min(3,6)<<endl;
    
    // similarly
    cout << sqrt(64)<<endl;  // square root
    cout << round(2.6)<<endl;  // roundoff
    cout << log(2)<<endl;      //natural log
    cout << cbrt(2)<<endl;   // cube root
    cout << pow(2,3)<<endl;  // power of x to power y

    return 0;
}